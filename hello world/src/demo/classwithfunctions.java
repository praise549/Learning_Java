package demo;

public class classwithfunctions {
	private int sum=0;
	private int i,j;
	public int Add(int a,int b) //function with arguement and return type
	{
		i=a;
		j=b;
		sum=i+j;;
		return sum;
	}
	
	public int Sub() //function without arguement and return type
	{
		i=50;
		j=30;
		sum=i-j;;
		return sum;
	}

}
